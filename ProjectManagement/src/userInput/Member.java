package userInput;

public class Member {

	//Variables
	private String Name;
	private String[] Projects;
	
	
	
	
	//Return
	/**
	 * @return the name
	 */
	public String getName() {
		return Name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		Name = name;
	}
	/**
	 * @return the projects
	 */
	public String[] getProjects() {
		return Projects;
	}
	/**
	 * @param projects the projects to set
	 */
	public void setProjects(String[] projects) {
		Projects = projects;
	} 
}
